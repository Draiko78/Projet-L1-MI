def update_map_with_plants(plants_dict):
    """Update gmap based on plants from database"""

    if not plants_dict:
        return
    
    for growth_state, positions in plants_dict.items():
        for pos in positions:
            if len(pos) >= 2:
                grid_x = pos[0] // 40
                grid_y = pos[1] // 40
                
                # Check bounds
                if 0 <= grid_y < len(gmap) and 0 <= grid_x < len(gmap[0]):
                    gmap[grid_y][grid_x] = growth_state


def extract_plants_from_map():
    plants_dict = {3: [], 4: [], 5: []}
    
    for y, row in enumerate(gmap):
        for x, value in enumerate(row):
            if value in [3, 4, 5]:
                pixel_x = x * 40
                pixel_y = y * 40
                plants_dict[value].append((pixel_x, pixel_y))
    
    return plants_dict